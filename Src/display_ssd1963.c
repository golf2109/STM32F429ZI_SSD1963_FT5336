#include "stm32f4xx_hal.h"
#include "display_ssd1963.h"

const uint8_t TFT_FONT[];
volatile uint8_t * WF43_DATA_ptr;
volatile uint8_t * WF43_COMMAND_ptr;

void SSD1963_init(void)
{
  volatile int t;
  
  WF43_DATA_ptr    = ((uint8_t *)0x60010000);
  WF43_COMMAND_ptr = ((uint8_t *)0x60000000);
  HAL_GPIO_WritePin(Reset_LCD_GPIO_Port, Reset_LCD_Pin, GPIO_PIN_SET);
  HAL_GPIO_WritePin(Reset_LCD_GPIO_Port, Reset_LCD_Pin, GPIO_PIN_RESET);
  for(t=0;t<10000;t++);
  HAL_GPIO_WritePin(Reset_LCD_GPIO_Port, Reset_LCD_Pin, GPIO_PIN_RESET);  
  for(t=0;t<10000;t++);

  SSD1963_lowlevelinit();
  FULL_ON(0x000000);
}

void TFT_putuint8_t(uint16_t x, uint16_t y, uint8_t c)
{
  uint8_t i,k;
  WindowSet(x,x+6,y,y+7);
  Write_Command(0x2c);
  for(k = 0; k < 8; k++) 
  {
    for(i=8;i>1;i--)
    {
      if((TFT_FONT[((int)c*8)+k] & 1<<i)!=0)
				SendData(0xffffff);
      else
				SendData(0x000000);      
    }
  }
  return;
}

void Write_Command(uint8_t cmd)
{
  *WF43_COMMAND_ptr = cmd;
}

//;******************************************************************************
void Write_Data(uint8_t data)
{
  *WF43_DATA_ptr = data;
}
//==============================================================
void Command_Write(uint8_t cmd,uint8_t data)
{
	*WF43_COMMAND_ptr = cmd;
	*WF43_DATA_ptr    = data;
}
//==============================================================
void SendData(uint32_t color)
{
	Write_Data((color)>>16); // color is red
	Write_Data((color)>>8);  // color is green
	Write_Data(color);  		 // color is blue
}
